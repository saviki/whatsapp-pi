function getLocaleOverride(): string | undefined {
    const envLocale = process.env.WHATSAPP_PI_LOCALE?.trim();
    if (envLocale) {
        return envLocale;
    }

    const argPrefix = '--whatsapp-pi-locale=';
    const explicitArg = process.argv.find(arg => arg.startsWith(argPrefix));
    if (explicitArg) {
        return explicitArg.slice(argPrefix.length).trim() || undefined;
    }

    const argIndex = process.argv.indexOf('--whatsapp-pi-locale');
    if (argIndex >= 0) {
        return process.argv[argIndex + 1]?.trim() || undefined;
    }

    return undefined;
}

function format(template: string | undefined, params: Params = {}): string {
    const value = template ?? '';
    return value.replace(/\{(\w+)\}/g, (_match, key) => String(params[key] ?? `{${key}}`));
}

export function t(key: Key, params?: Params): string {
    const locale = currentLocale as Locale | undefined;
    const template = locale ? translations[locale]?.[key] : undefined;
    return format(template ?? fallback[key], params);
}

export function resetI18n(): void {
    currentLocale = undefined;
}

export function initI18n(pi: ExtensionAPI): void {
    const forcedLocale = getLocaleOverride();
    if (forcedLocale) {
        currentLocale = forcedLocale;
    }

    pi.events?.emit?.("pi-core/i18n/registerBundle", {
        namespace,
        defaultLocale: "en",
        fallback,
        translations,
    });
    pi.events?.on?.("pi-core/i18n/localeChanged", (event: unknown) => {
        if (forcedLocale) {
            return;
        }

        if (event && typeof event === "object" && "locale" in event) {
            const locale = String((event as { locale?: unknown }).locale ?? "");
            if (locale) {
                currentLocale = locale;
            }
        }
    });
    pi.events?.emit?.("pi-core/i18n/requestApi", {
        namespace,
        onApi(api: { getLocale?: () => string | undefined }) {
            if (forcedLocale) {
                return;
            }

            const locale = api.getLocale?.();
            if (locale) {
                currentLocale = locale;
            }
        },
    });
}
